                                <h3><b>Low CIBIL Score</b></h3>
                                <p>
                                    Some of the factors leading to low score are :
                                </p>
                                <p> 
                                    <b><i>Outstanding Credit Card Balance</i></b> - Some borrowers do not pay their total credit card bills every month and only pay the minimum amount due, which leaves them with an outstanding balance on their cards. What they do not realise is that this can reduce their credit score.
                                </p>  
                                <p>
                                    <b><i>Too Many Loans/Credit Cards</i></b> - If a borrower applies for too many loans or credit cards, then every time a bank checks his/her credit report or score for processing the loan or credit card application it is counted as a hard enquiry. Too many hard enquiries can result in reduction of the score.
                                </p>
                                <p>
                                    <b><i>High credit usage</i></b> - If a borrower uses over 30% of his/her credit limit, then it negatively affects his/her score as it shows his/her dependency on credit is high.
                                </p>
                                <p>
                                    <b><i>Delay in repayments</i></b> - If a borrower delays in making his/her EMI payments, then it will be noted in his/her CIBIL report, which will reduce his/her credit score. Repayments are one of the important factors that is considered while calculating the credit score
                                </p>                                 
